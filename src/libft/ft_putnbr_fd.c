#include "../../include/libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	long long int	temp;

	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(n * -1, fd);
	}
	else
	{
		temp = n;
		if (temp > 9)
		{
			ft_putnbr_fd(temp / 10, fd);
			ft_putnbr_fd(temp % 10, fd);
		}
		else
			ft_putchar_fd('0' + n, fd);
	}
}
